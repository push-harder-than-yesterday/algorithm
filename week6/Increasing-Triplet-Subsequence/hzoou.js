function increasingTriplet(numbers) {
  let a = Infinity;
  let b = Infinity;

  for (const number of numbers) {
    if (number <= a) {
      a = number;
    } else if (number <= b) {
      b = number;
    } else {
      return true;
    }
  }

  return false;
}

